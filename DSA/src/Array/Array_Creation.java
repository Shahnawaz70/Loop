package Array;

public class Array_Creation {
	public static void main(String[] args) {
		// Array Creation
		int [] arr = new int [4];
		
		//array population
		arr[0] = 10;
		arr[1] = 20;
		arr[2] = 30;
		arr[3] = 40;
		
		for(int i =0; i<arr.length; i++) {
			System.out.println(arr[i]);
		}
	}
}


